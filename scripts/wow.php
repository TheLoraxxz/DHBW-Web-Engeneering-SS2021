    <h2><b>E-Sports</b></h2>
      <p>
        Die größte und bekannteste dieser oben erwähnten Herausforderungen ist der sogenannte Raid. Eine Gruppe von 20 Spielern versucht zusammen extrem starke Gegner zu bezwingen. Die neue Eröffnung eines Raids
        in World of Warcraft ist ein großes Event. Auch in Hinsicht auf den E-Sport. Viele Organisationen beteiligen sich an dem daraus hervorgehenden "Race to World First". Die besten Gilden versuchen
        den Endgegner als erstes zu bezwingen. Dieses "Rennen" kann sich über mehrere Wochen hinweg ziehen und die Fans können den Vortschritt der jeweiligen Gruppen in diversen Liveübertragungen nachverfolgen.
        Im nachfolgenden Video ist die Gilde "Limit" zu sehen, welche den Endgegner "Sire Denathrius" im aktuellen Raid "Schloss Nathria" weltweit zuerst bezwingt. Auch ist die dazu notwendige Koordinierung der
        Gruppe zu sehen. Jeder der zwanzig Spieler hat eine genau definierte Rolle und die erdachte Taktik zum besiegen des Gegners muss genauestens ausgeführt werden. 
