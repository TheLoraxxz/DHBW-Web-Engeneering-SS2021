    <p>
      Die verschiedenen Klassen werden nochmals in unterschiedliche Rollen aufgeteilt. So gibt es sogenannte Tanks, Heiler und DDs. 
      Ein "Tank" ist ein Charakter mit starker Rüstung, welcher die Gegner im Kampf an sich bindet und verhindert, dass sie seine Gruppenmitglieder angreifen. Der Heiler hingegen, stellt mit Hilfe verschiedener 
      Zaubersprüche die durch Angriffe verlorenen Lebenspunkte wieder her, oder schützt seine Gruppenmitglieder mit schadensabsorbierenden Schildern. Die Damage Dealer (DDs) sind die Schadensausteiler. Hierbei wird zwischen Fernkämpfern und Nahkämpfern unterschieden. Die Fernkämpfer sind 
      in der Regel Charaktere welche Zauber wirken (Magier, Hexenmeister). Die Nahkämpfer suchen hingegen die direkte Konfrontation mit dem Gegner.
