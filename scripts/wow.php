  <h2><b>Spielprinzip</b></h2>
    <p>
      Ist der Charakter ersteinmal erstellt, kann das Abenteuer in der weiten Welt von Azeroth beginnen. Das Ziel des Spiels ist es, den Charakter immer weiter zu entwickeln. Dazu können Aufgaben, sogenannte Quest erfüllt werden
      um Erfahrungspunkte zu sammeln. Hat man genug Erfahrungspunkte gesammelt steigt man ein Level auf. Dieser Prozess wiederholt sich, bis die maximale Stufe erreicht ist. Sobald dieses Ziel abgeschlossen wurde, kann in den "End-Content"
      eingestiegen werden. Hier geht das Spiel erst richtig los. Viele Spieler organisieren sich hierzu in Gilden, vergleichbar mit Vereinen in den wahren Welt. Sofern einige willige Mitstreiter gefunden sind, kann gemeinsam mit einer Gruppe aus anderen Spielern versucht werden schwierige Herausforderungen zu meistern. Als Belohnung kann dabei
      neue und vorallem hochwertigere Ausrüstung erhalten werden. Dadurch wird der Charakter des Spielers immer stärker und kann auch immer schwierigere Aufgaben meißtern. 
    </p>
