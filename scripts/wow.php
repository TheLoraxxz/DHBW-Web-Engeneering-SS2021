    <p>
    Die genanten Rassen organisieren sich gemeinsam in zwei rivalisierenden Fraktionen. So gehören die Nachtelfen zusammen mit den Zwergen, Dreanai, Gnomen und Worgen zu der von den Menschen 
    angeführten Allianz. Die Blutelfen, Orcs, Tauren, Trolle, Untote und Goblins bilden gemeinsam die sogenante Horde. Die einzige Außnahme in dieser Struktur bilden die Pandaren. Spieler die diese Rasse wählen, müssen später im Spielgeschehen 
    entscheiden, welcher der beiden Fraktionen sie sich anschließen möchten.
    </p>
    <p>
      Nachdem diese Wahl getroffen ist, kann der Charakter individualisiert werden. Hierbei gibt es je nach Rasse eine Auswahl zwischen verschiedenen Merkmalen. 
      Beispielsweise kann die Gesichtsform, die Frisur oder die Hautfarbe ausgewählt werden. Aber auch rassenspezifische Eigenschaften wie z.B Hörner bei den Tauren oder die Ohren der Elfen bieten mehrere Auswahlmöglichkeiten.
    </p>
